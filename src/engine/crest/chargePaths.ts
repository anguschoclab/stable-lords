// Helper to get all charge paths by type
export function getChargePathsByType(type: ChargeType): Record<string, ChargePath> {
  switch (type) {
    case 'beast':
      return BEAST_PATHS;
    case 'weapon':
      return WEAPON_PATHS;
    case 'symbol':
      return SYMBOL_PATHS;
    case 'nature':
      return NATURE_PATHS;
    case 'celestial':
      return CELESTIAL_PATHS;
    case 'mythical':
      return MYTHICAL_PATHS;
    default:
      return BEAST_PATHS;
  }
}

// Helper to get a random charge from a type
export function getRandomCharge(type: ChargeType, seed: number): ChargePath & { name: string } {
  const paths = getChargePathsByType(type);
  const keys = Object.keys(paths);
  const key = keys[Math.floor(seed * keys.length)] || keys[0]!;
  return { ...paths[key]!, name: key };
}
